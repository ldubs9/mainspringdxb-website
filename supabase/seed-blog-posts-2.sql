<p>Whatever watch you are considering, start with these three questions:</p>

<p><strong>1. Is the reference number confirmed?</strong> As I described in my inspection guide, the reference number tells you everything — what movement should be inside, what the dial should look like, what the case dimensions are. If the seller cannot confirm the reference number, walk away.</p>

