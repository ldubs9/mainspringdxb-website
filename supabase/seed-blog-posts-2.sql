<p>The good news is that fakes — even well-made ones — leave traces. Here is what to look for.</p>

<h2>Start with the Weight</h2>

<p>A genuine vintage watch from a respected manufacturer has weight. Not necessarily heavy — a thin dress watch will be light — but the weight should feel solid and consistent with the case material. Fakes are often slightly lighter because the metal alloys used are cheaper. This is a first impression, not a conclusion, but it is worth noting.</p>

<h2>Examine the Dial Under Magnification</h2>

<p>The dial is one of the hardest things to fake well. Under magnification, look for:</p>

<ul>
<li><strong>Font consistency.</strong> The printing on genuine dials is precise and even. Fakes often show slight irregularities in letter spacing, weight, or alignment.</li>
<li><strong>Depth of the text.</strong> On genuine printed dials, the text sits cleanly on the surface. On fakes, the printing can appear slightly raised, smeared at the edges, or inconsistent in ink density.</li>
<li><strong>Lume condition.</strong> Vintage lume ages in specific ways — tritium turns a warm cream or gold colour over decades, and the aging is consistent across all lume plots. If the hands and the dial lume age differently, one or both have been replaced.</li>
<li><strong>"Swiss Made" text.</strong> On genuine Swiss watches, this text follows specific conventions for each era. Knowing what the correct text looks like for your reference and period is important — fakers often get this wrong.</li>
</ul>

