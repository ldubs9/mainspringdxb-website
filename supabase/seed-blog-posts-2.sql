<p>On a genuine vintage mechanical watch, the crown engages smoothly and the winding action has a consistent, quality feel. The crown itself should match the reference — size, shape, and logo engraving (if present) are all reference-specific. A crown that feels loose, rough, or simply wrong compared to other examples of the same reference is a red flag.</p>

<h2>Look at the Caseback</h2>

